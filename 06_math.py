import math

print(5 / 2)    # 2.5
print(5 // 2)   # 2
print(-3 // 2)  # -2, the // operator will always round down
print(int(-3 / 2)) # -1, int(-1.5) will round to zero = -1

print(5 % 3) # 2


print(math.fmod(-5, 3))  # -2

print(math.floor(1.5))   # 1
print(math.ceil(1.5))    # 2
print(math.sqrt(2))      # 1.4
print(math.pow(5, 2))    # 25

# infinity and -infinity
float("int")
float("-inf")

# python number are infinite & never overflow
